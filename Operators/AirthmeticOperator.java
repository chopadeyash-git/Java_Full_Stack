class AirthmeticOperator 
{
	public static void main(String[] args) 
	{
		int a=11;
		int b=2;

		System.out.println("a+b = "+(a+b));
		System.out.println("a-b = "+(a-b));
		System.out.println("a*b = "+(a*b));
		System.out.println("a/b = "+(a/b));
		System.out.println("a%b = "+(a%b));


		int c=10;
		System.out.println(--c);
		System.out.println(c--);
		
		System.out.println(++c);
		System.out.println(c++);

	}
}
